#include "3-calc.h"
/**
 * op_add - uses the add operator
 * @a: first int
 * @b: second int
 *
 * Return: the result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - uses the sub operator
 * @a: first int
 * @b: second int
 *
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - uses the mul operator
 * @a: first int
 * @b: second int
 *
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - uses the div operator
 * @a: first int
 * @b: second int
 *
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - uses the mod operator
 * @a: first int
 * @b: second int
 *
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
